with base as (
select request_at,status,count(request_at) over (partition by request_at) as tot_cnt,
(case when status like 'cancelled%' then count(request_at) over  (partition by request_at,status) else 0 end) as canc_cnt
    from trips t where not exists(
select 1 from users u where t.client_id = u.users_id and u.banned = 'Yes')
    #and request_at = '2013-10-01'
),
result as (
select request_at ,status,tot_cnt,canc_cnt,row_number() over (partition by request_at order by canc_cnt desc) as rn from base)
select request_at as "day",round(canc_cnt/tot_cnt,2) as "cancellation rate" from result where rn = 1;
